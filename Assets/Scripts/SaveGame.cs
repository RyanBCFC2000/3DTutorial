using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class SaveGame
{
    float playerPosX;       
    float playerPosY;
    float playerPosZ;
 
     public SaveGame(float newPosX, float newPosY, float newPosZ)
     {
         playerPosX=newPosX;
         playerPosY=newPosY;
         playerPosZ=newPosZ;

     }


}
